	<h1 class="mb-6 text-2xl font-bold">About BoAT</h1>

	<div class="prose prose-sm space-y-4 text-sm">
		<p>
			<strong>BoAT</strong> (Bogazici University Annotation Tool) is a web-based tool for creating and annotating
			treebanks, conforming to the <a href="https://universaldependencies.org/" class="text-primary hover:underline" target="_blank" rel="noopener">Universal Dependencies</a>
			(UD) format. It is designed for morphologically rich languages (MRLs) and supports collaborative annotation workflows.
		</p>

		<h2 class="text-lg font-semibold">Features</h2>
		<ul class="list-disc space-y-1 pl-5">
			<li>CoNLL-U file import/export with round-trip fidelity</li>
			<li>Inline annotation editor with keyboard shortcuts</li>
			<li>Inter-annotator agreement computation</li>
			<li>Adjudication workflow with diff view</li>
			<li>Sentence-level discussion threads</li>
			<li>Per-treebank validation profiles</li>
			<li>Structured FEATS/MISC field parsing</li>
			<li>Full-text search across all annotation fields</li>
		</ul>

